import SwiftUI

extension Color {
    static let pokemonCell = Color("PokemonCell")
    static let sectionHeader = Color("SectionHeader")
    /// Pokemon Color
    static let cBlack = Color("CBlack")
    static let cBlue = Color("CBlue")
    static let cBrown = Color("CBrown")
    static let cGray = Color("CGray")
    static let cGreen = Color("CGreen")
    static let cPink = Color("CPink")
    static let cPurple = Color("CPurple")
    static let cRed = Color("CRed")
    static let cWhite = Color("CWhite")
    static let cYellow = Color("CYellow")
}

